# suma
x = 55
y = 3
print("Suma: ", x + y)

# resta
print("Resta: ", x - y)

# multiplicación
print("Multiplicación: ", x * y)

# división
print("División: ", x / y)

# módulo
print("Módulo: ", x % y)

# potencia
print("Potencia: ", x ** y)

# division entera
print("División entera: ", x // y)

# precedencia
print("Precedencia: ", (x + y) * (x - y))

# comprobar de x es par
print("Par: ", x % 2 == 0)

# comprobar de x es impar
print("Impar: ", x % 2 != 0)

